WITH Sel AS (SELECT B_Q_ID, 
SUM(CASE WHEN Col = 'R' THEN B_VOL ELSE 0 END) AS R,
SUM(CASE WHEN Col = 'G' THEN B_VOL ELSE 0 END) AS G,
SUM(CASE WHEN Col = 'B' THEN B_VOL ELSE 0 END) AS B
FROM (SELECT *, (SELECT V_COLOR FROM utV WHERE V_ID = B_V_ID) AS Col
FROM utB) AS T
GROUP BY B_Q_ID)

SELECT
255*((SELECT COUNT(*) FROM utQ) - (SELECT COUNT(*) FROM Sel)) + SUM(255 - R) R,
255*((SELECT COUNT(*) FROM utQ) - (SELECT COUNT(*) FROM Sel)) +  SUM(255 - G) G,
255*((SELECT COUNT(*) FROM utQ) - (SELECT COUNT(*) FROM Sel)) + SUM(255 - B) B
FROM Sel
