SELECT (SELECT Q_NAME FROM utQ WHERE T.B_Q_ID = Q_ID), R FROM 
(SELECT B_Q_ID,
SUM(CASE WHEN Col = 'R' THEN B_VOL ELSE 0 END) R,
SUM(CASE WHEN Col = 'G' THEN B_VOL ELSE 0 END) G,
SUM(CASE WHEN Col = 'B' THEN B_VOL ELSE 0 END) B FROM 
(SELECT *, (SELECT V_COLOR FROM utV WHERE V_ID = B_V_ID) Col FROM utB) AS X
GROUP BY B_Q_ID) AS T
WHERE R = G AND G = B AND B = R AND R > 0 AND R < 255
